package String;

public class TipoString {
	
	private static final String EVT = "ST300EVT";
	private static final String ALT = "ST300ALT";
	private static final String EMG = "ST300EMG";
	private static final String STT = "ST300STT";
	
	
	public static String getEvt() {
		return EVT;
	}
	public static String getAlt() {
		return ALT;
	}
	public static String getEmg() {
		return EMG;
	}
	public static String getStt() {
		return STT;
	}
	
}
